public class Semaforo {
    
    private int posX;
    private int posY;
    private int posRojoX;
    private int posRojoY;
    private int posAmarilloX;
    private int posAmarilloY;
    private int posVerdeX;
    private int posVerdeY;
    private boolean estado;

    public int getPosX() {
        return posX;
    }

    public void setPosX(int posX) {
        this.posX = posX;
    }

    public int getPosY() {
        return posY;
    }

    public void setPosY(int posY) {
        this.posY = posY;
    }

    public int getPosRojoX() {
        return posRojoX;
    }

    public void setPosRojoX(int posRojoX) {
        this.posRojoX = posRojoX;
    }

    public int getPosRojoY() {
        return posRojoY;
    }

    public void setPosRojoY(int posRojoY) {
        this.posRojoY = posRojoY;
    }

    public int getPosAmarilloX() {
        return posAmarilloX;
    }

    public void setPosAmarilloX(int posAmarilloX) {
        this.posAmarilloX = posAmarilloX;
    }

    public int getPosAmarilloY() {
        return posAmarilloY;
    }

    public void setPosAmarilloY(int posAmarilloY) {
        this.posAmarilloY = posAmarilloY;
    }

    public int getPosVerdeX() {
        return posVerdeX;
    }

    public void setPosVerdeX(int posVerdeX) {
        this.posVerdeX = posVerdeX;
    }

    public int getPosVerdeY() {
        return posVerdeY;
    }

    public void setPosVerdeY(int posVerdeY) {
        this.posVerdeY = posVerdeY;
    }

    public boolean isEstado() {
        return estado;
    }

    public void setEstado(boolean estado) {
        this.estado = estado;
    }
    
    
    
}
